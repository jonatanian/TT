									<p>Tener una sección llamada “Recursos” donde se encontrarán organizados los diferentes materiales de apoyo del CMPL para sus cursos, capacitaciones, presentaciones y eventos.</p>
								</li>
								<li>
									<span class="fa fa-leaf"></span>
