									<p>El acceso al sistema a cada trabajador del CMPL con un único usuario y contraseña para acceder a su correspondencia (oficios y memorándums). Existirán cuatro tipos de usuario: el administrador, que es el jefe del Departamento de Sistemas y Banco de Datos; la oficialía de partes, los directivos y los administrativos.</p>
								</li>
								<li>
									<span class="fa fa-wrench"></span>
									<h3>Información de contacto</h3>
									<p>Una pantalla que desplegará el directorio del CMPL con la información actualizada.</p>
								</li>
								<li>
									<span class="fa fa-leaf"></span>
									<h3>Galería fotográfica</h3>
									<p>Una pantalla donde se podrán ver y editar los álbumes de fotografías digitales del CMPL desplegados en forma cronológica y por eventos.</p>
								</li>
								<li>
									<span class="fa fa-wrench"></span>
									<h3>Escudos y logos oficiales en un solo lugar</h3>
									<p>Descarga de logotipos oficiales y actuales de las diferentes instancias con las que el CMPL se relaciona.</p>
								</li>
							</ul>
						</div>
						<div class="4u">
