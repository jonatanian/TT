						<div class="4u">
							<ul class="special-icons">
								<li>
									<span class="fa fa-cogs"></span>
									<h3>Oficios entrantes</h3>
									<p>La Oficialía de Partes podrá registrar los oficios entrantes al CMPL, mediante una pantalla que contenga un formulario que permita registrar cada oficio con los siguientes datos: Número de oficio, fecha de redacción, área que emite, emisor, cargo del emisor, destinatario, cargo del destinatario, dependencia, asunto, fecha de acuse, nombre de quien entrega y si requiere o no respuesta.</p>
								</li>
								<li>
									<span class="fa fa-wrench"></span>
									<h3>Oficios salientes</h3>
									<p>Los directivos y administrativos podrán registrar oficios salientes mediante una pantalla que contenga un formulario que permita registrar cada oficio con los siguientes datos: Número de oficio, fecha de redacción, área que emite, emisor, cargo del emisor, destinatario, cargo del destinatario, dependencia, asunto, fecha de acuse, nombre de quien entrega y si requiere o no respuesta.</p>
								</li>
								<li>
									<span class="fa fa-leaf"></span>
									<h3>Memorándums</h3>
									<p>Los directivos y administrativos podrán registrar memorándums mediante una pantalla que contenga un formulario que permita registrar cada memorándum con los siguientes datos: fecha de redacción, área que emite, emisor, cargo del emisor, destinatario, cargo del destinatario, dependencia, asunto, fecha de acuse, nombre de quien entrega y si requiere o no respuesta. Con esto se llevará un control digital de documentos de correspondencia formal.</p>
								</li>
								<li>
									<span class="fa fa-wrench"></span>
									<h3>Notificaciones al día</h3>
									<p>Utilizar la sección “Avisos” para publicar notificaciones a todos los trabajadores referentes a las siguientes actividades del CMPL mediante un formulario que contenga el título del aviso, su fecha de publicación, su descripción y el nombre del usuario que lo publicó.</p>
								</li>
							</ul>
						</div>
						<div class="4u">
							<ul class="special-icons">
								<li>
									<span class="fa fa-cogs"></span>
									<h3>Correspondencia accesible desde cualquier lugar</h3>
									<p>Cuando se registre un nuevo documento de correspondencia se guardará en formato PDF una copia escaneada del documento de correspondencia en un repositorio digital para su consulta futura. Así, en caso de que el documento físico se extravíe se tendrá siempre a la mano una copia digital del mismo.</p>
								</li>
								<li>
									<span class="fa fa-wrench"></span>
									<h3>Información siempre disponible</h3>
									<p>Cada usuario en SISA-CMPL contará con una pantalla donde podrá consultar el registro de toda su correspondencia a manera de historial, ya sea general, por número de oficio o por fecha de registro. Con esta pantalla el usuario podrá ver únicamente su correspondencia ordenada en un solo lugar, sin necesidad de tener copias físicas del documento.</p>
								</li>
								<li>
									<span class="fa fa-leaf"></span>
									<h3>Seguimiento sencillo</h3>
									<p>El sistema contará con una función que notificará al usuario qué documentos ha atenido ya. De esta manera, el usuario llevará un mejor control de aquello que ya atendió y lo que está pendiente aún en cuanto a correspondencia formal. Se podrá llevar un seguimiento de los oficios o memorándums hasta que concluyan sus peticiones. Con esto evitamos la falta de seguimiento.</p>
								</li>
								<li>
									<span class="fa fa-cogs"></span>
									<h3>Cursos de P+L</h3>
									<p>Una pantalla con información actualizada de cursos impartidos en el CMPL en forma de tabla, así como la modificación de la misma por parte del administrador.</p>
								</li>
							</ul>
						</div>
						<div class="4u">
							<ul class="special-icons">
								<li>
									<span class="fa fa-cogs"></span>
